--[[

ESOTERIA:

A fantasy MMORPG that takes place at an unknown time in ancient history. 

PREMISE: 

A massive thriving civilization once existed, being the primary superpower of their explored universe. They were
leading in all civil developments like technology, language, social structure, and more. Their most
prominent advancement was in their understanding and manipulation of magic and spells. 

One of their most impressive feats was a combination of their technological and magical abilities--creating a 
universal world door that acted as a gate between other worlds, where the Esoterians could freely roam to 
explore unknown territories or interact with their allies.

For thousands of year, they've thwarted off many great threats, posing extinction-level danger to themselves 
and their allies. However, this would soon change. One day, a mysterious portal began to form
in the middle of Esoteria's central town. A dark army of unknown origin and leader emerged from this portal,
reeking havoc on all the Esoterians. Even with their incredibly developed magic and technology, they were
caught by complete surprise and were no match for this new mysterious power. As the invaders continued their
rampage, they destroyed the keystone that powered all world gates, trapping the Esoterians on their planet.
As the keystone was destroyed, shards of it flew everywhere, some entering the gate before it went inactive.
This caused keystone shards to spread randomly all throughout different worlds.

After this dark force invaded, the Esoterians fled to a village kept so secret, not even all Esoterians
knew about it's existence. This was a strategic back up plan in case Esoteria ever fell. Luckily, one
keystone shard was recovered by the king of the Esoterians, right before fleeing to the hidden village.

OBJECTIVE:

The player spawns in this hidden village, immediately approached by [unknown character]. They explain the
entire situation to the player, and reveal that they have one gate shard which can transport them to one 
world.

The player's main storyline is to play through each world, collecting a gate shard at the very end. This gate
shard unlocks the next world for them to visit. Once they have collected all the gate shards, they can rebuild
the portal and find the leader of the evil invaders as a major boss fight.

